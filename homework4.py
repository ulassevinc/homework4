userBirthYear = int( input( 'Please enter your birth year:'))

if userBirthYear in range(1928, 1945):
    print('You are silent Generation')
if userBirthYear in range(1946, 1964):
    print('You are Baby Boomers')
if userBirthYear  in range(1965, 1980): 
    print('You are Generation X')
if userBirthYear  in range(1981, 1996):  
    print('You are Generation Y')
if userBirthYear in range(1997, 2012): 
    print('You are Generation Z')
if userBirthYear in range(2012, 2021):  
    print('You are Generation Alpha')
    

    
                    
                    